package class7;

public class forLoopsHW7 {
    public static void main(String[] args) {
        for (int number=20; number<=50; number++){
            if(number%2!=0){
                System.out.println(number);
            }
        }


        for (int n= 50; n>=20; n--){
            if(n%2!=0){
                System.out.println(n);
            }
        }




    }
}