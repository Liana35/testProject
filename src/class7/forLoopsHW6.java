package class7;

public class forLoopsHW6 {
    public static void main(String[] args) {
        for (int number=1; number<=20; number++) {
            if (number % 2 == 0) {
                System.out.println(number);
            }

        }
        for (int num=20; num>=1; num--) {
            if (num % 2== 0) {
                System.out.println(num);
            }


        }
    }
}